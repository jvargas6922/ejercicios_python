# USO DE CONDICIONALES IF, ELIF, ELSE.

# EJEMPLO 1
x = 10
if x > 5:
    print("x es mayor a 5")
elif x < 15:
    print("x es menor a 15")
else:
    print("x es mayor a 15")

# EJEMPLO 2
edad = int(raw_input("Ingrese su edad: "))

if edad >= 18:
    print("Usted es mayor de edad")
else:
    print("Usted es menor de edad")

