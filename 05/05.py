# -*- coding: utf-8 -*-
# OPERADORES ARITMETRICOS
x = 10
y = 5
# SUMA (+)
print("Este es el resultado de la suma: ", x + y)

# RESTA (-)
print("Este es el resultado de la resta: ", x - y)

# MULTIPLICACION (*)
print("Este es el resultado de la multiplicacion: ", x * y)

# DIVISION(/)
print("Este es el resultado de la division: ", x / y)

# POTENCIA
print("Este es el resultado de la potencia: ", x ** y)

# MODULO
print("Este es el resultado del modulo: ", x % y)

# PRODUCTO ENTERO DE UNA DIVISION
print("Este es el resultado del producto entero de una division: ", x // y)

# OPERADOR DE COMPARACION

# IGUAL QUE (==)
print("Este es el resultado de la comparacion: ", x == y)

# MAYOR QUE (>)
print("Este es el resultado de la comparacion: ", x > y)

# MENOR QUE (<)
print("Este es el resultado de la comparacion: ", x < y)

# MAYOR O IGUAL QUE (>=)
print("Este es el resultado de la comparacion: ", x >= y)

# MENOR O IGUAL QUE (<=)
print("Este es el resultado de la comparacion: ", x <= y)
